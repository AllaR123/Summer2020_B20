package day08_if_Statement;

public class If_Statement {

    public static void main(String[] args) {

        int a = 10;
        int b = 20;

        if(a > b) {
            System.out.println(a + " is larger than " + b);
        } if(a < b) {
            System.out.println(a + " is less than " + b);
        } if(a == b) {
            System.out.println(a + " is equals to " + b);
        }

    }
}
