package day04_Variables;

public class variables_Practiice {

    public static void main(String[] args) {

        byte num1 = 100;
        int num2 = 10000;
        double num3 = 3.5;
        float num4 = 2.5f;
        long num5 = 999999999999L;
        int num6 = 850000;

        System.out.println(num1 + ", " + num2 + ", " + num3 + ", " + num4 + ", " +
                num5 + ", "+ num6);
    }
}
